package bean;

public class ContactService {
	
	private String Id;
	private String serviceText;
	private String fund;
	private String fundCode;
	private String contact;
	private String investorAccountId;
	private String investorAccountCode;
	private String accountId;
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getServiceText() {
		return serviceText;
	}
	public void setServiceText(String serviceText) {
		this.serviceText = serviceText;
	}
	public String getFund() {
		return fund;
	}
	public void setFund(String fund) {
		this.fund = fund;
	}
	public String getFundCode() {
		return fundCode;
	}
	public void setFundCode(String fundCode) {
		this.fundCode = fundCode;
	}
	public String getContact() {
		return contact;
	}
	public void setContact(String contact) {
		this.contact = contact;
	}
	public String getInvestorAccountId() {
		return investorAccountId;
	}
	public void setInvestorAccountId(String investorAccountId) {
		this.investorAccountId = investorAccountId;
	}
	public String getInvestorAccountCode() {
		return investorAccountCode;
	}
	public void setInvestorAccountCode(String investorAccountCode) {
		this.investorAccountCode = investorAccountCode;
	}
	public String getAccountId() {
		return accountId;
	}
	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}
	
	
	

}
